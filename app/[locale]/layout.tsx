import { Locale, routing } from '@/i18n/routing'
import type { Metadata } from 'next'
import { NextIntlClientProvider } from 'next-intl'
import { getMessages } from 'next-intl/server'
import { notFound } from 'next/navigation'
import { Almarai, Cairo, Nunito } from 'next/font/google'
import './globals.css'
import NewNavbar from '@/components/navbar/NewNavbar'
import NewHero from '@/components/hero/NewHero'
import WhyNojeed from '@/components/WhyNojeed/WhyNojeed'

const almarai = Almarai({
    variable: '--font-almarai',
    subsets: ['arabic', 'latin'],
    weight: ['300', '400', '700', '800'],
})
const nunito = Nunito({
    variable: '--font-almarai',
    subsets: ['latin'],
    weight: ['300', '400', '700', '800'],
})

const cairo = Cairo({
    variable: '--font-cairo',
    subsets: ['arabic', 'latin'],
    weight: ['400', '500', '700'],
})

export const metadata: Metadata = {
    title: 'Create Next App',
    description: 'Generated by create next app',
}

export default async function RootLayout({
    children,
    params,
}: Readonly<{
    children: React.ReactNode
    params: { locale: Locale }
}>) {
    const { locale } = await params
    if (!routing.locales.includes(locale as Locale)) {
        notFound()
    }
    // Providing all messages to the client
    // side is the easiest way to get started
    const messages = await getMessages()
    return (
        <html dir={locale === 'ar' ? 'rtl' : 'ltr'} lang={locale}>
            <body
                className={`  ${nunito.variable} ${almarai.variable} ${cairo.variable} antialiased max-w-[1919px] mx-auto`}
            >
                <NextIntlClientProvider messages={messages}>
                    <main className="relative overflow-hidden">
                        <NewHero />
                        <div className="absolute top-0 left-1/2 -translate-x-1/2 w-full max-w-7xl px-4 pt-6 z-20">
                            <NewNavbar />
                        </div>
                        <div className="relative z-10 mt-[-100vh] pt-[100vh] px-6">
                            {children}
                        </div>
                        <WhyNojeed />
                    </main>
                </NextIntlClientProvider>
            </body>
        </html>
    )
}
